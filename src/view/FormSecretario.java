    private void excludeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excludeBtnActionPerformed
        int linha = tableSecretarios.getSelectedRow();
        int codigo;
        Secretario sExcluir = null;
        if (linha != -1) {
            int colunas = tableSecretarios.getColumnCount();
            for (int x = 0; x < colunas; x++) {
                if (tableSecretarios.getColumnName(x).equals("C√≥digo")) {
                    codigo = (int) tableSecretarios.getValueAt(linha, x);
                    sExcluir = cp.findByCodigo(codigo);

                }
            }
            if (sExcluir != null) {
                int op = JOptionPane.showConfirmDialog(null, "Deseja mesmo ecluir " + sExcluir.getNome() + "?");
                if (op == 0) {
                    try {
                        cp.delete(sExcluir);
                        JOptionPane.showMessageDialog(null, "Excluido com sucesso");
                        DefaultTableModel dtm = (DefaultTableModel) tableSecretarios.getModel();
                        dtm.removeRow(linha);

                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Este secret√°rio possui registros vinculados.\nN√£o foi poss√≠vel realizar a exclusao!");
                    }
                }

            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um secret√°rio!");
        }
    }//GEN-LAST:event_excludeBtnActionPerformed

