    private void botAddAtivActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botAddAtivActionPerformed
        boolean flag = false;
        Itemdeatividade it = new Itemdeatividade();
        int linha = tableAtividades.getSelectedRow();
        int codigo;
        if (linha != -1) {
            if (!txtDuracao.getText().equals("") && txtSeries.getText().equals("") && txtRepeticoes.getText().equals("")) {
                it.setDuracao(Integer.parseInt(txtDuracao.getText()));
                flag = true;
            } else if (!txtSeries.getText().equals("") && !txtRepeticoes.getText().equals("") && txtDuracao.getText().equals("")) {
                it.setSeries(Integer.parseInt(txtSeries.getText()));
                it.setRepeticoes(Integer.parseInt(txtRepeticoes.getText()));
                flag = true;
            } else {
                labelWarningForm.setText("Digite séries e repetições ou duração");
                warningPanelForm.setBackground(new Color(255, 51, 51));
                btnError.setBackground(new Color(255, 51, 51));
                warningPanelForm.setVisible(true);
                txtSeries.setText("");
                txtRepeticoes.setText("");
                txtDuracao.setText("");
                txtSeries.setValue(null);
                txtRepeticoes.setValue(null);
                txtDuracao.setValue(null);
            }
            if (flag == true) {
                Atividade a;
                int colunas = tableAtividades.getColumnCount();
                for (int x = 0; x < colunas; x++) {
                    if (tableAtividades.getColumnName(x).equals("Código")) {
                        codigo = (int) tableAtividades.getValueAt(linha, x);
                        a = contAti.findByCodigo(codigo);
                        it.setFicha(ficha);
                        it.setAtividade(a);
                        listaItens.add(it);
                        atualizaTabelaItens();
                    }
                }
            }

        } else {

            labelWarningForm.setText("Selecione uma atividade.");
            warningPanelForm.setBackground(new Color(255, 51, 51));
            btnError.setBackground(new Color(255, 51, 51));
            warningPanelForm.setVisible(true);

            //timer
        }


    }//GEN-LAST:event_botAddAtivActionPerformed

