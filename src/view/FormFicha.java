      private void atualizaTabelaItens() {

                DefaultTableModel dtm = (DefaultTableModel) tabelItens.getModel();
                dtm.setNumRows(0);
                for (Itemdeatividade s : listaItens) {
                    dtm.addRow(new Object[]{s.getAtividade().getDescricao(), s.getSeries()==null?"-": s.getSeries(),
                    s.getRepeticoes()==null?"-": s.getRepeticoes(),
                    s.getDuracao()==null?"-": s.getDuracao()});
                }
 
    }
    private void atualizaTabelaAtividade() {
        new Thread() {
            @Override
            public void run() {
                System.out.println("jnf");
                listaAtivides.clear();
                listaAtivides.addAll(contAti.findByDescricao(txtPesquisaAtividade.getText()));
                DefaultTableModel dtm = (DefaultTableModel) tableAtividades.getModel();
                dtm.setNumRows(0);
                for (Atividade s : listaAtivides) {
                    dtm.addRow(new Object[]{s.getCodigo(), s.getDescricao(), s.getMusculo().equals("") ? "-" : s.getMusculo()});
                }
            }
        }.start();
    }
